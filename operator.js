// >>>>> OPERATOR <<<<<
// +, -, /, *, %
console.log(1 + 1) // Penjumlahan
console.log(1 - 1) // Pengurangan
console.log(1 / 1) // Pembagian
console.log(1 * 1) // Perkalian
console.log(5 % 2) // 1 - Modulus: Sisa pembagian
console.log(10 % 2) // 0 - Modulus: Sisa pembagian
console.log(10 % 6) // 4 - Modulus: Sisa pembagian


// String Concate: Apapun yang dijumlahkan dengan string, maka akan di join 
